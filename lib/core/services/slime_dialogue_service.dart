  /// 获取适合当前情境的对话文案
  String getDynamicDialogue() {
    final name = UserState().userName.value.isNotEmpty
        ? UserState().userName.value
        : 'Stitch';

    // 优先级 1: 频率羁绊 (久别重逢比时间更重要)
    final days = UserState().daysSinceLastVisit;
    if (days >= 7) {
      return _replaceName(_getRandom(_longTimeNoSee), name);
    } else if (days >= 3) {
      return _replaceName(_getRandom(_fewDaysNoSee), name);
    }

    // 优先级 2: 每日打卡 + 时间流转 (合并池以增加多样性)
    final hour = DateTime.now().hour;
    List<String> pool = [];

    if (hour >= 6 && hour < 10) {
      pool.addAll(_morningGreetings);
    } else if (hour >= 10 && hour < 18) {
      pool.addAll(_dayGreetings);
    } else if (hour >= 18 && hour < 24) {
      pool.addAll(_eveningGreetings);
    } else {
      pool.addAll(_lateNightGreetings);
    }

    // 如果是近期访问过，混入“每日贴贴”和“引导”文案
    pool.addAll(_dailyGreetings);
    if (_random.nextDouble() < 0.3) {
      pool.addAll(_interactionPrompts);
    }

    return _replaceName(_getRandom(pool), name);
  }

  /// 戳一戳对话（交互触发）
  String getPokeDialogue() {
    final name = UserState().userName.value.isNotEmpty
        ? UserState().userName.value
        : 'Stitch';
    return _replaceName(_getRandom(_interactionPrompts), name);
  }

  String _replaceName(String text, String name) =>
      text.replaceAll('{name}', name);

  String _getRandom(List<String> list) => list[_random.nextInt(list.length)];

